package Chap04.Sec01;

public class s01_1 {
    public static void main(String[] args) {
        int score = 93;

        if (score >= 90) {
            System.out.println("점수가 90점 이상입니다.");
            System.out.println("합격입니다!");
        } else {
            System.out.println("점수가 90점 이하입니다.");
            System.out.println("불합격입니다");
        }
    }
}
